package java_programming_language.conditional_statements.if_else_;

public class largest_of_two {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        if (a >= b) {
            System.out.println("A is largest of 2");
        } else {
            System.out.println("B is largest of 2");
        }
    }
}
